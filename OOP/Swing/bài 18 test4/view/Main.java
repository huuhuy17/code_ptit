    private void jComboBox3ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox3ItemStateChanged
        // TODO add your handling code here:
        if(jComboBox3.getSelectedIndex()==1){
            listSV.sort(new Comparator<SinhVien>() {
                @Override
                public int compare(SinhVien o1, SinhVien o2) {
                    String[] ten1=o1.getTenSV().split("\\s+");
                    String[] ten2=o2.getTenSV().split("\\s+");  
                    if(ten1[ten1.length-1].equalsIgnoreCase(ten2[ten2.length-1])){
                        return o1.getTenSV().compareToIgnoreCase(o1.getTenSV());
                    }
                    else{
                        return ten1[ten1.length-1].compareToIgnoreCase(ten2[ten2.length-1]);
                    }
                }
            
        });
        }
        if(jComboBox3.getSelectedIndex()==2){
            listSV.sort(new Comparator<SinhVien>(){
                @Override
                public int compare(SinhVien o1, SinhVien o2) {
                    String[] ngay1=o1.getNgaysinh().split("/");
                    String[] ngay2=o2.getNgaysinh().split("/");  
                    if(ngay1[ngay1.length-1].equalsIgnoreCase(ngay2[ngay2.length-1])){
                        return o1.getNgaysinh().compareToIgnoreCase(o1.getNgaysinh());
                    }
                    else{
                        return ngay1[ngay1.length-1].compareToIgnoreCase(ngay2[ngay2.length-1]);
                    }
                     
                }
                
            });
        }
        tmSV.setRowCount(0);
        for(SinhVien i:listSV){
            tmSV.addRow(i.toObject());
        }
    }//GEN-LAST:event_jComboBox3ItemStateChanged
