    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        int n=listSV.size();
        if(n>0){
            SinhVien.setSma(listSV.get(n-1).getMaSV()+1);
        }
        jTextField1.setText(SinhVien.getSma()+"");// hiển thị mã
        
        String tenSV,diachi,ngaysinh,lop;
        
       
        try{
            //maDV=Integer.parseInt(jTextField1.getText());
            SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            df.setLenient(false);
            tenSV=jTextField2.getText();
            diachi=jTextField3.getText();
            ngaysinh=jTextField4.getText();
            df.parse(ngaysinh);
            lop=jTextField7.getText();
            SinhVien s = new SinhVien(tenSV, diachi, ngaysinh, lop);
            tmSV.addRow(s.toObject());
            listSV.add(s);
        }catch(TrongException e){
            JOptionPane.showMessageDialog(this,"Không được để trống");
            return;
        }catch(DienThoaiException e){
            JOptionPane.showMessageDialog(this,"Nhập vào đúng định dạng lớp");
            return;
        }catch(ParseException e){
             JOptionPane.showMessageDialog(this,"Nhập đúng định dạng ngày dd/MM/yyyy");
            return;
        }
    }//GEN-LAST:event_btThemActionPerformed

