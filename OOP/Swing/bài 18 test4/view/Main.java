    private void btThem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThem2ActionPerformed
        // TODO add your handling code here:
        int masv=Integer.parseInt(jComboBox1.getSelectedItem().toString());
        int mamh=Integer.parseInt(jComboBox5.getSelectedItem().toString());
        if(trung2Ma(masv, mamh)){
            JOptionPane.showMessageDialog(this,"trùng 2 mã");
            jComboBox1.requestFocus();
            return;
        }
        double diem;
        try{
            diem=Double.parseDouble(jTextField12.getText());
            BangDiem bd=new BangDiem(getSVByMa(masv),getMHByMa(mamh),diem);
            tmBD.addRow(bd.toObject());
            lBD.add(bd);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Nhập số");
            jTextField12.requestFocus();
            return;
        }catch(SoLuongException e){
            JOptionPane.showMessageDialog(this,"Nhập số");
            jTextField12.requestFocus();
            return;
        }
    }//GEN-LAST:event_btThem2ActionPerformed

